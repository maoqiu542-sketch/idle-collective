const path = require('node:path')
const sharp = require('sharp')
const { svgForAsset } = require('./bootstrap-placeholders')
const {
  assetSourceDir,
  ensureDir,
  loadSpecs,
  nextCandidateVariant,
  parseArgs,
  readAssetMeta,
  writeAssetMeta
} = require('./shared')

const args = parseArgs(process.argv.slice(2))
const requestedAssetId = typeof args.asset === 'string' ? args.asset : null
const requestedCategory = typeof args.category === 'string' ? args.category : null
const requestedVariant = typeof args.variant === 'string' ? args.variant : null
const limit = typeof args.limit === 'string' ? Number(args.limit) : 4
const strategy = typeof args.strategy === 'string' ? args.strategy : 'local_template'
const strategyNote = typeof args.note === 'string' ? args.note : 'Generated by local template fallback.'

function hashString(value) {
  let hash = 0
  for (let index = 0; index < value.length; index += 1) {
    hash = Math.imul(31, hash) + value.charCodeAt(index) | 0
  }
  return hash >>> 0
}

function mulberry32(seed) {
  let t = seed >>> 0
  return function random() {
    t += 0x6D2B79F5
    let x = Math.imul(t ^ (t >>> 15), 1 | t)
    x ^= x + Math.imul(x ^ (x >>> 7), 61 | x)
    return ((x ^ (x >>> 14)) >>> 0) / 4294967296
  }
}

function range(random, min, max) {
  return min + (max - min) * random()
}

function pick(random, values) {
  return values[Math.floor(random() * values.length)]
}

function overlaySvg(asset, width, height, seed, generationStrategy) {
  const random = mulberry32(seed)
  const fragments = []
  if (generationStrategy.includes('baseline_reset')) {
    return Buffer.from(`<?xml version="1.0" encoding="UTF-8"?>
<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 ${width} ${height}">
</svg>`)
  }
  const profile = generationStrategy.includes('template_swap')
    ? 'template_swap'
    : generationStrategy.includes('prompt_tuned')
      ? 'prompt_tuned'
      : generationStrategy.includes('fallback')
        ? 'fallback'
        : 'seed_explore'

  if (asset.category === 'terrain') {
    const terrainPalette = {
      grass: ['#BDE28A', '#5B8745', '#89BD6C'],
      forest: ['#1F4526', '#5A8E53', '#87B26B'],
      mountain: ['#C6B39A', '#6F5A49', '#9B856E'],
      water: ['#D7F2FF', '#7FC5EA', '#8EDCFF'],
      sand: ['#F6E8C2', '#D8BF8B', '#C9B075'],
      snow: ['#FFFFFF', '#DDEBFA', '#F6FBFF']
    }
    const palette = terrainPalette[asset.target_key] || terrainPalette.grass
    const ellipseCount = profile === 'template_swap' ? 64 : profile === 'prompt_tuned' ? 54 : 42
    const lineCount = profile === 'template_swap' ? 96 : profile === 'prompt_tuned' ? 80 : 64
    for (let index = 0; index < ellipseCount; index += 1) {
      const x = range(random, -12, width + 12)
      const y = range(random, -12, height + 12)
      const rx = range(random, width * 0.02, profile === 'template_swap' ? width * 0.1 : width * 0.14)
      const ry = rx * range(random, 0.45, 1.3)
      const opacity = range(random, 0.04, profile === 'fallback' ? 0.2 : 0.16)
      fragments.push(
        `<ellipse cx="${x.toFixed(1)}" cy="${y.toFixed(1)}" rx="${rx.toFixed(1)}" ry="${ry.toFixed(1)}" fill="${pick(random, palette)}" opacity="${opacity.toFixed(2)}" transform="rotate(${range(random, 0, 180).toFixed(1)} ${x.toFixed(1)} ${y.toFixed(1)})" />`
      )
    }
    for (let index = 0; index < lineCount; index += 1) {
      const y = range(random, -6, height + 6)
      fragments.push(
        `<line x1="-8" y1="${y.toFixed(1)}" x2="${(width + 8).toFixed(1)}" y2="${(y + range(random, -6, 6)).toFixed(1)}" stroke="${pick(random, palette)}" stroke-width="${range(random, 0.4, profile === 'template_swap' ? 1.2 : 1.6).toFixed(2)}" opacity="${range(random, 0.04, profile === 'fallback' ? 0.22 : 0.18).toFixed(2)}" stroke-linecap="round" />`
      )
    }
    if (profile !== 'seed_explore') {
      for (let index = 0; index < 28; index += 1) {
        const x = range(random, -4, width + 4)
        const y = range(random, -4, height + 4)
        const radius = range(random, width * 0.01, width * 0.035)
        fragments.push(
          `<circle cx="${x.toFixed(1)}" cy="${y.toFixed(1)}" r="${radius.toFixed(1)}" fill="${pick(random, palette)}" opacity="${range(random, 0.05, 0.14).toFixed(2)}" />`
        )
      }
    }
  } else {
    const iconPalette = ['#FFFFFF', '#F5E6D3', '#E8B4B8', '#A8D8B9', '#9B8AA5']
    for (let index = 0; index < 12; index += 1) {
      const x = range(random, width * 0.15, width * 0.85)
      const y = range(random, height * 0.15, height * 0.85)
      const radius = range(random, width * 0.03, width * 0.08)
      fragments.push(
        `<circle cx="${x.toFixed(1)}" cy="${y.toFixed(1)}" r="${radius.toFixed(1)}" fill="${pick(random, iconPalette)}" opacity="${range(random, 0.04, 0.12).toFixed(2)}" />`
      )
    }
    for (let index = 0; index < 16; index += 1) {
      const x1 = range(random, width * 0.2, width * 0.8)
      const y1 = range(random, height * 0.2, height * 0.8)
      fragments.push(
        `<line x1="${x1.toFixed(1)}" y1="${y1.toFixed(1)}" x2="${(x1 + range(random, -24, 24)).toFixed(1)}" y2="${(y1 + range(random, -24, 24)).toFixed(1)}" stroke="${pick(random, iconPalette)}" stroke-width="${range(random, 1, 3).toFixed(2)}" opacity="${range(random, 0.04, 0.14).toFixed(2)}" stroke-linecap="round" />`
      )
    }
  }

  return Buffer.from(`<?xml version="1.0" encoding="UTF-8"?>
<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 ${width} ${height}">
  ${fragments.join('\n  ')}
</svg>`)
}

async function generateCandidate(asset, variant) {
  const existingMeta = readAssetMeta(asset)
  const attemptNumber = (existingMeta?.attempt_count ?? 0) + 1
  const seed = hashString(`${asset.asset_id}:${variant}:${attemptNumber}`)
  const sourceDir = assetSourceDir(asset)
  const destinationPath = path.join(sourceDir, `${asset.asset_id}__${variant}__${asset.version}.png`)
  ensureDir(sourceDir)

  const baseSvg = Buffer.from(svgForAsset(asset))
  const overlay = overlaySvg(asset, asset.source_resolution.width, asset.source_resolution.height, seed, strategy)
  const profile = strategy.includes('template_swap')
    ? { brightness: 1.01, saturation: 1.08, hue: seed % 7 }
    : strategy.includes('prompt_tuned')
      ? { brightness: 0.99, saturation: 1.02, hue: seed % 9 }
      : strategy.includes('baseline_reset')
        ? { brightness: 1.0, saturation: 1.0, hue: 0 }
      : strategy.includes('fallback')
        ? { brightness: 1.0, saturation: 1.1, hue: seed % 5 }
        : { brightness: 0.97 + ((seed % 7) * 0.01), saturation: 0.94 + ((seed % 5) * 0.03), hue: seed % 11 }
  let pipeline = sharp(baseSvg)
    .resize(asset.source_resolution.width, asset.source_resolution.height, { fit: 'cover' })
    .modulate(profile)

  if (!strategy.includes('baseline_reset')) {
    pipeline = pipeline.composite([{ input: overlay }])
  }

  await pipeline
    .png()
    .toFile(destinationPath)

  const now = new Date().toISOString()
  const candidates = [
    ...(existingMeta?.candidates ?? []).filter(candidate => candidate.variant !== variant),
    {
      variant,
      path: destinationPath,
      source_kind: strategy,
      imported_at: now
    }
  ]
  const importedVariants = [
    ...(existingMeta?.imported_variants ?? []),
    {
      variant,
      source_file: null,
      imported_file: destinationPath,
      imported_at: now,
      strategy
    }
  ]
  const attemptHistory = [
    ...(existingMeta?.attempt_history ?? []),
    {
      attempt: attemptNumber,
      variant,
      strategy,
      queued_at: now,
      imported_at: now,
      source_file: null,
      imported_file: destinationPath,
      outcome: 'imported',
      note: strategyNote
    }
  ]

  writeAssetMeta(asset, {
    ...existingMeta,
    candidates,
    imported_variants: importedVariants,
    attempt_count: attemptNumber,
    attempt_history: attemptHistory,
    review_queue: {
      status: 'pending_review',
      candidate_variant: variant,
      updated_at: now,
      note: strategyNote
    },
    pipeline_state: 'imported',
    ai_review: {
      decision: 'pending',
      reviewer_version: existingMeta?.ai_review?.reviewer_version
    },
    generated_at: now
  })

  console.log(`[art:local-generate] ${asset.asset_id} ${variant} attempt=${attemptNumber}`)
}

async function main() {
  const { catalog } = loadSpecs()
  let emitted = 0

  for (const asset of catalog.assets) {
    if (requestedAssetId && asset.asset_id !== requestedAssetId) {
      continue
    }
    if (requestedCategory && asset.category !== requestedCategory) {
      continue
    }
    if (emitted >= limit) {
      break
    }

    const meta = readAssetMeta(asset)
    const variant = requestedVariant || nextCandidateVariant(meta)
    await generateCandidate(asset, variant)
    emitted += 1
  }
}

main().catch(error => {
  console.error('[art:local-generate] failed', error)
  process.exitCode = 1
})
